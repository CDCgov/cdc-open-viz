import type { Meta, StoryObj } from '@storybook/react-vite'
import { within, expect } from 'storybook/test'
import Chart from '../CdcChartComponent'
import {
  assertVisualizationRendered,
  waitForPresence,
  waitForEditor,
  openAccordion,
  MIN_ANIMATION_DELAY_MS
} from '@cdc/core/helpers/testing'
import altTextConfig from './_mock/alt_text_metadata.json'

const meta: Meta<typeof Chart> = {
  title: 'Components/Templates/Chart/Accessible Alt Text',
  component: Chart,
  parameters: {
    docs: {
      description: {
        component:
          'Demonstrates SVG accessibility: auto-generated aria-label with optional configurable description concatenated into the label for reliable screen reader support.'
      }
    }
  }
}

export default meta
type Story = StoryObj<typeof Chart>

export const MetadataDescription: Story = {
  args: {
    config: {
      ...altTextConfig,
      altText: { type: 'metadata', metadataKey: 'altDescription' }
    },
    isEditor: false
  },
  parameters: {
    docs: {
      description: {
        story:
          'Description pulled from dataMetadata is concatenated into the aria-label after the auto-generated title for reliable screen reader support.'
      }
    }
  },
  play: async ({ canvasElement }) => {
    await assertVisualizationRendered(canvasElement)
    const svg = await waitForPresence('svg[role="img"]', canvasElement)

    const expected = `Line chart with the title: COVID-19 Test Positivity. ${altTextConfig.dataMetadata.altDescription}`
    expect(svg?.getAttribute('aria-label')).toBe(expected)
  }
}

